package com.springboot.model;

public class CartaResult {
	
	private String id_carta;
	private String nombre_carta;
	
	public String getId_carta() {
		return id_carta;
	}
	public void setId_carta(String id_carta) {
		this.id_carta = id_carta;
	}
	public String getNombre_carta() {
		return nombre_carta;
	}
	public void setNombre_carta(String nombre_carta) {
		this.nombre_carta = nombre_carta;
	}
	
	

}
